#include <unistd.h>

int	ft_len(char *str)
{
	int	len;

	len = 0;
	while (*str != '\0')
	{
		str++;
		len++;
	}
	return (len);
}

int	main(int argc, char **argv)
{
	int	i;

	i = 1;
	while (i < argc)
	{
		write(1, argv[i], ft_len(argv[i]));
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
